package au.gov.ansto.bragg.common.dra.algolib.rebinning;

/**
 * Static library of mathematical utilities used during the re-binning process.
 * 
 * @author lwi
 */
public class MathUtil {

	/**
	 * Find the greatest common divisor of two integers. This is done using 
	 * the Euclidean Algorithm.
	 */
	public static int gcd(int a, int b) {
		if (b == 0) {
			return a;
		} else {
			return gcd(b, a % b);
		}
	}
}
